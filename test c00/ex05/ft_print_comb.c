#include <unistd.h>

void	ft_print_comb(void)
{
	char a[3];

	a[0] = '0';
	while (a[0] <= '9')
	{
		a[1] = a[0];
		while (a[1] <= '9')
		{
			a[2] = a[1];
			while (a[2] <= '9')
			{
				if (a[0] != a[1] && a[1] != a[2])
				{
					write(1, a, 3);
					if (a[0] != '7')
						write(1, ", ", 2);
				}
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
